    public function store(Request $request): RedirectResponse
    {
        $request->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'lowercase', 'email', 'max:255', 'unique:' . User::class],
            'password' => ['required', 'confirmed', Rules\Password::defaults()],
            'image' => ['image', 'mimes:jpeg,png,jpg,gif,webp,bmp,svg,tiff,ico', 'max:92160'], // 90MB limit
        ]);                                           //  (Realistic maximum safe limit: 100MB (102400 KB))


        // Not uploaded image will be default image
        $imagepath = 'profiles/default.png';


        // Check if the image is uploaded
        // If the image is uploaded, store it in the public disk
        // and set the image path to the stored image path
        // If the image is not uploaded, set the image path to the default image path
        if ($request->hasFile('image')) {
            $imagepath = $request->file('image')->store('profiles', 'public');
        }

        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'image' => $imagepath,
        ]);

        event(new Registered($user));

        Auth::login($user);

        return redirect(route('dashboard', absolute: false));
    }
